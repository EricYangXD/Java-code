public class ChangeTest {
	public static void main(String args[]) {
		byte b = 15;
		int i = b;
		long l = b;
		float f = b;
		double d = b;
		System.out.println(b);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		int s = 99;
		byte n = (byte) s;
		System.out.println(s);
		System.out.println(n);
	}
}
