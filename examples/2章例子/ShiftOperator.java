public class ShiftOperator {
	public static void main(String[] args) {
		int number = 1;
		System.out.println("2的0次方: " + number);
		number = number << 1;
		System.out.println("2的1次方: " + number);
		number = number << 1;
		System.out.println("2的2次方: " + number);
		number = number << 1;
		System.out.println("2的3次方：" + number);
	}
}
